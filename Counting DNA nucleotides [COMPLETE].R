nucleotideCount <- function(DNAseq) {
  splitSeq = strsplit(DNAseq, "")[[1]]
  # Tables display each given item and how frequently they appear
  nucCount = as.data.frame(table(splitSeq))
  nucCount[,"Freq"]
}

